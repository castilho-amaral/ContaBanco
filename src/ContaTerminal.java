public class ContaTerminal {

   Integer Numero;
   String Agencia;
   String Nome_Cliente;
   Double Saldo;

    public ContaTerminal(Integer Numero, String Agencia, String Nome_Cliente, Double Saldo) {
         this.Numero = Numero;
         this.Agencia = Agencia;
         this.Nome_Cliente = Nome_Cliente;
         this.Saldo = Saldo;
    }
}
